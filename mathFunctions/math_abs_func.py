#!/usr/bin/env python3
#Python3 abs() 函数
#Python3 数字 Python3 数字
#描述
#abs() 函数返回数字的绝对值。
#语法
#以下是 abs() 方法的语法:
#abs( x )
#参数
#x -- 数值表达式，可以是整数，浮点数，复数。
#返回值
#函数返回 x（数字）的绝对值，如果参数是一个复数，则返回它的大小。
#实例
#以下展示了使用 abs() 方法的实例：
#实例
#!/usr/bin/python3
print ("abs(-40) : ", abs(-40))
print ("abs(100.10) : ", abs(100.10))
#以上实例运行后输出结果为：
#
#abs(-40) :  40
#abs(100.10) :  100.1
#输入是复数（complex number）时， 返回这个复数的模。
