#!/usr/bin/env python3
#Python3 lower()方法
#Python3 字符串 Python3 字符串
#描述
#Python lower() 方法转换字符串中所有大写字符为小写。
#语法
#lower()方法语法：
#str.lower()
#参数
#无。
#返回值
#返回将字符串中所有大写字符转换为小写后生成的字符串。
#实例
#以下实例展示了lower()的使用方法：
#!/usr/bin/python3
str = "Runoob EXAMPLE....WOW!!!"
print( str.lower() )
