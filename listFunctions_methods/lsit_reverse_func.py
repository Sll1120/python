#!/usr/bin/env python3
#Python3 List reverse()方法
#Python3 列表 Python3 列表
#描述
#reverse() 函数用于反向列表中元素。
#语法
#reverse()方法语法：
#list.reverse()
#参数
#NA。
#返回值
#该方法没有返回值，但是会对列表的元素进行反向排序。
#实例
#以下实例展示了 reverse()函数的使用方法：
#!/usr/bin/python3
list1 = ['Google', 'Runoob', 'Taobao', 'Baidu']
list1.reverse()
print ("列表反转后: ", list1)
#以上实例输出结果如下：
#列表反转后:  ['Baidu', 'Taobao', 'Runoob', 'Google']
