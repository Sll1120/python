#!/usr/bin/env python3
#Python3 swapcase()方法
#Python3 字符串 Python3 字符串
#描述
#Python swapcase() 方法用于对字符串的大小写字母进行转换，即将大写字母转换为小写字母，小写字母会转换为大写字母。
#语法
#swapcase() 方法语法：
#str.swapcase();
#参数
#NA。
#返回值
#返回大小写字母转换后生成的新字符串。
#实例
#以下实例展示了 swapcase() 函数的使用方法：
#实例
#!/usr/bin/python
str = "RUNOOB!!!";
print ( str.swapcase() );
str = "runoob!!!";
print ( str.swapcase() );
str = "abCDE!!!";
print ( str.swapcase() );
#以上实例输出结果如下：
#runoob!!!
#RUNOOB!!!
#ABcde!!!
