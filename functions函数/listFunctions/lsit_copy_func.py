#!/usr/bin/env python3
#Python3 List copy()方法
#Python3 列表 Python3 列表
#描述
#copy() 函数用于复制列表，类似于 a[:]。
#语法
#copy()方法语法：
#list.copy()
#参数
#无。
#返回值
#返回复制后的新列表。
#实例
#以下实例展示了 copy()函数的使用方法：
#实例
#!/usr/bin/python3
list1 = ['Google', 'Runoob', 'Taobao', 'Baidu']
list2 = list1.copy()
print ("list2 列表: ", list2)
#以上实例输出结果如下：
#list2 列表:  ['Google', 'Runoob', 'Taobao', 'Baidu']
