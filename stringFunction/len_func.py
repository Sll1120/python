#!/usr/bin/env python3
#Python3 len()方法
#Python3 字符串 Python3 字符串
#描述
#Python len() 方法返回对象（字符、列表、元组等）长度或项目个数。
#语法
#len()方法语法：
#len( s )
#参数
#s -- 对象。
#返回值
#返回对象长度。
#实例
#以下实例展示了 len() 的使用方法：
str = "runoob"
print(len(str))             # 字符串长度
#6
l = [1,2,3,4,5]
print(len(l))               # 列表元素个数
#5
t = ('name','sll','age','33')
print(len(t))               # 列表元素个数
